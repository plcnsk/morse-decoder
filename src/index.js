function decode(expr) {

  const morseLetters = {
    '0000000010': 'A',
    '0000000011': 'B',
    '0000000100': 'C',
    '0000000101': 'D',
    '0000000110': 'E',
    '0000000111': 'F',
    '0000001000': 'G',
    '0000001001': 'H',
    '0000001010': 'I',
    '0000001011': 'J',
    '0000001100': 'K',
    '0000001101': 'L',
    '0000001110': 'M',
    '0000001111': 'N',
    '0000010000': 'O',
    '0000010001': 'P',
    '0000010010': 'Q',
    '0000010011': 'R',
    '0000010100': 'S',
    '0000010101': 'T',
    '0000010110': 'U',
    '0000010111': 'V',
    '0000011000': 'W',
    '0000011001': 'X',
    '0000011010': 'Y',
    '0000011011': 'Z',
    '**********': ' ',
  };

  let result = '';

  for (let i = 0; i < expr.length; i += 10) {
    const part = expr.slice(i, i + 10);

    const morsePart = part.replace(/10/g, '.').replace(/11/g, '-');

    result += morseLetters[morsePart];
  }

  return result;

}

module.exports = {
    decode
}